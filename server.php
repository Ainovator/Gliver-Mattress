<?php
// Получаем данные из тела запроса
$data = json_decode(file_get_contents('php://input'), true);

// Проверяем, что данные успешно получены
if (isset($data['width'])) {
    // Сохраняем значение ширины матраса в переменную
    $mattressWidth = $data['width'];

    // Возвращаем успешный ответ
    echo "Значение ширины матраса: $mattressWidth мм";
} else {
    // Если данные не были получены, возвращаем ошибку
    http_response_code(400);
    echo "Ошибка: Не удалось получить данные.";
}
?>
